import Foundation
import UIKit

extension UIColor {
    static var whiteApple: UIColor {
        return UIColor(red: 255/255, green: 255/255, blue: 255/255, alpha: 1)
    }
    
    static var grayApple: UIColor {
        return UIColor(red: 240/255, green: 240/255, blue: 240/255, alpha: 1)
    }
    
    static var blackApple: UIColor {
        return UIColor(red: 28/255, green: 28/255, blue: 30/255, alpha: 1)
    }
    
    static var blueApple: UIColor {
        return UIColor(red: 10/255, green: 132/255, blue: 255/255, alpha: 1)
    }
    
    static var darkerBlueApple: UIColor {
        return UIColor(red: 0/255, green: 109/255, blue: 217/255, alpha: 1)
    }
}
