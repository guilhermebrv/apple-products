import Foundation
import UIKit

extension UIColor {
    static var whiteApple: UIColor {
        return UIColor(red: 245, green: 245, blue: 247, alpha: 1)
    }
    
    static var grayApple: UIColor {
        return UIColor(red: 192, green: 192, blue: 192, alpha: 0.5)
    }
}
